//===========================================================================
//==================== SIMPLIFIED PRESSURE ADVANCE (SPA) v4.5 ===============
//===========================================================================
// SPA — Simplified Pressure Advance для Marlin FT Motion
// Дифференциальная модель (K·ΔVe) с опциональным EMA-сглаживанием скорости.
//
// Для активации необходимы ОБА макроса: SIMPLIFIED_PA и PA_LOOKAHEAD.
//
// Новое в v4.5 (аудит-рекомендации):
//   - EMA-фильтр низких частот для Ve (SPA_EMA_ALPHA) — Task 1
//   - Мягкое клиппирование offset (SPA_SOFT_CLAMP) — Task 2
//   - Телеметрия с пиковым offset (SPA_PEAK_TRACKING) — Task 3
//   - Динамический PA_MAX_P_MM через M900 L<value> — Task 4
//   - Плавный сброс offset при ретрактах (SPA_RETRACT_DECAY)
// Подробнее: docs/SPA_Documentation.md

#if ENABLED(FT_MOTION)
  #define SIMPLIFIED_PA                     // Включает функции установки/сброса K, reset_state
  #define PA_LOOKAHEAD                      // Включает основной алгоритм в calc_traj_point() и поля block_t

  #if ENABLED(SIMPLIFIED_PA)

    // ──────────────────────────────────────────────────────────────────────────
    // Task 1: Сглаживание скорости (EMA Low-Pass Filter)
    // ──────────────────────────────────────────────────────────────────────────
    // Коэффициент EMA-фильтра для Ve. Значение 0.0 = нет сглаживания, 1.0 = максимальное.
    // Рекомендуемый диапазон: 0.05–0.30. При FTM_FS=5000Гц, alpha=0.15 даёт τ≈1.3мс.
    // Без фильтрации (alpha=0) микро-сегменты слайсера создают пилообразные выбросы Ve,
    // что ведёт к рывкам экструдера и step loss (см. SPA_Documentation.md §2.3).
    #define SPA_EMA_ALPHA          0.15f     // τ ≈ (1-α)/(α·FTM_FS) [c]

    // ──────────────────────────────────────────────────────────────────────────
    // Task 2: Мягкое клиппирование (Soft Saturation / Anti-Windup)
    // ──────────────────────────────────────────────────────────────────────────
    // Включает замену жёсткого clamp() на экспоненциальное затухание превышения.
    // Жёсткий clamp приводит к "ослеплению" интегратора: offset застревает на
    // пределе, вызывая волнообразные артефакты (z-banding). Soft-clamp позволяет
    // offset кратковременно превышать PA_MAX_P_MM с экспоненциальным затуханием,
    // сохраняя линейность отклика при снижении скорости.
    #define SPA_SOFT_CLAMP                  // Раскомментировать для включения

    // ──────────────────────────────────────────────────────────────────────────
    // Task 3: Телеметрия и пиковый offset
    // ──────────────────────────────────────────────────────────────────────────
    #define SPA_TELEMETRY                   // Вывод PA|K=... Ve=... в терминал
    #define SPA_PEAK_TRACKING               // Отслеживание пикового offset за последние N кадров

    // ──────────────────────────────────────────────────────────────────────────
    // Task 4: Параметры ограничения
    // ──────────────────────────────────────────────────────────────────────────
    // Максимальная компенсация PA (мм). При превышении включается клиппирование.
    // Для мягкого клиппирования (SPA_SOFT_CLAMP) offset может кратковременно
    // превышать этот лимит, но экспоненциально затухает к нему.
    // Можно менять на лету через M900 L<value>.
    #define PA_MAX_P_MM            2.0f      // Лимит давления (мм). M900 L<value>

    // Коэффициент затухания offset при ретрактах (битовый сдвиг).
    // 0 = отключено (offset остаётся неизменным, возможны рывки при ΔVe)
    // 1 = затухание 1/2 за кадр (очень агрессивно)
    // 4 = затухание 1/16 за кадр (рекомендуется)
    // 8 = затухание 1/256 за кадр (медленно)
    #define SPA_RETRACT_DECAY      4         // >> 4 = decay 1/16 per frame при !pa_extruding

    // ─── Тюнинг через G-код (runtime) ────────────────────────────────────────
    // M900 K<value>   — установка коэффициента PA (0.0–2.0 с)
    // M900 L<value>   — установка PA_MAX_P_MM (0.1–10.0 мм)
    // M900 E<alpha>   — установка SPA_EMA_ALPHA (0.0–1.0)
    // ──────────────────────────────────────────────────────────────────────────

  #endif
#endif
